# Addition of integer
a=4
b=5
c=3.4
print(f'Addition of {a} and {b}:{a+b}')
print(f'Addition of {a} and {c}:{a+c}')


#subtraction of integer
print(f'subtraction of {a} and {b}:{a-b}')
print(f'subtraction of {a} and {c}:{a-c}')

#multiplication 
print(f'Multiplication of {a} and {b}:{a*b}')
print(f'Multiplication of {a} and {b}:{a*c}')

# Division 
print(f'Division of {a} and {b}:{a/b}')
print(f'Division of {a} and {c}:{a/c}')
print(f'Floor Division of {a} and {b}:{a//b}')


# modulus
print(f'Modulus of {a} and {b}:{a%b}')
print(f'Modulus of {a} and {c}:{a%c}')
print(f'power :{a**b}')
